CREATE TABLE research_topics (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    student_id NUMBER NOT NULL,
    title VARCHAR2(255) NOT NULL,
    description CLOB,
    academic_field_id NUMBER,
    status VARCHAR2(20) DEFAULT 'IDEA' NOT NULL,
    created_at DATE DEFAULT SYSDATE NOT NULL,
    updated_at DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT pk_research_topics PRIMARY KEY (id),
    CONSTRAINT fk_research_topics_student FOREIGN KEY (student_id) REFERENCES students (id) ON DELETE CASCADE,
    CONSTRAINT fk_research_topics_field FOREIGN KEY (academic_field_id) REFERENCES academic_fields (id),
    CONSTRAINT ck_research_topics_status CHECK (status IN ('IDEA', 'READING', 'IN_PROGRESS', 'COMPLETED'))
);

CREATE TABLE research_collections (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    research_topic_id NUMBER NOT NULL,
    title VARCHAR2(255) NOT NULL,
    collection_type VARCHAR2(20) NOT NULL,
    resource_url VARCHAR2(1000),
    summary CLOB,
    keywords VARCHAR2(1000),
    reading_status VARCHAR2(20) DEFAULT 'TO_READ' NOT NULL,
    created_at DATE DEFAULT SYSDATE NOT NULL,
    updated_at DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT pk_research_collections PRIMARY KEY (id),
    CONSTRAINT fk_research_coll_topic FOREIGN KEY (research_topic_id) REFERENCES research_topics (id) ON DELETE CASCADE,
    CONSTRAINT ck_research_coll_type CHECK (collection_type IN ('PAPER', 'DATASET', 'LINK', 'NOTE', 'VIDEO')),
    CONSTRAINT ck_research_coll_status CHECK (reading_status IN ('TO_READ', 'READING', 'READ', 'IMPORTANT'))
);

CREATE TABLE reviews (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    student_id NUMBER NOT NULL,
    reviewable_type VARCHAR2(20) NOT NULL,
    reviewable_id NUMBER NOT NULL,
    rating NUMBER(1) NOT NULL,
    comment_text CLOB,
    created_at DATE DEFAULT SYSDATE NOT NULL,
    updated_at DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT pk_reviews PRIMARY KEY (id),
    CONSTRAINT fk_reviews_student FOREIGN KEY (student_id) REFERENCES students (id) ON DELETE CASCADE,
    CONSTRAINT ck_reviews_type CHECK (reviewable_type IN ('RESOURCE', 'TEMPLATE', 'TOOL')),
    CONSTRAINT ck_reviews_rating CHECK (rating BETWEEN 1 AND 5)
);

CREATE TABLE audit_logs (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    table_name VARCHAR2(100) NOT NULL,
    record_id NUMBER NOT NULL,
    action_type VARCHAR2(20) NOT NULL,
    old_value CLOB,
    new_value CLOB,
    changed_by NUMBER,
    created_at DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT pk_audit_logs PRIMARY KEY (id),
    CONSTRAINT fk_audit_changed_by FOREIGN KEY (changed_by) REFERENCES users (id),
    CONSTRAINT ck_audit_action_type CHECK (action_type IN ('INSERT', 'UPDATE', 'DELETE', 'APPROVE', 'REJECT'))
);

CREATE INDEX idx_students_university ON students (university_id);
CREATE INDEX idx_students_field ON students (academic_field_id);
CREATE INDEX idx_documents_student ON academic_documents (student_id);
CREATE INDEX idx_resources_status ON resources (status);
CREATE INDEX idx_resources_field ON resources (academic_field_id);
CREATE INDEX idx_templates_status ON templates (status);
CREATE INDEX idx_templates_field ON templates (academic_field_id);
CREATE INDEX idx_research_student ON research_topics (student_id);
CREATE INDEX idx_reviews_reviewable ON reviews (reviewable_type, reviewable_id);

