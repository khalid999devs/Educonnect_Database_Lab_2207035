    template_id NUMBER NOT NULL,
    amount NUMBER(10,2) DEFAULT 0 NOT NULL,
    payment_status VARCHAR2(20) DEFAULT 'PENDING' NOT NULL,
    created_at DATE DEFAULT SYSDATE NOT NULL,
